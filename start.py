number % 2

# 최대값을 구하는 함수
max(2, 3, 4)

# 최소값을 구하는 함수
min(2, 3, 4)

# 반올림 함수
# 소수 첫째 자리인 .14는 0.5 미만이기 때문에 내림되어 3이 됩니다
# round(3.14159, 2) -> 결과: 3.14 (소수점 둘째 자리까지)
round(3.14)

# 절댓값 함수
# 부호는 무시하고 숫자 크기만 남깁니다
abs(-3)

# 거듭제곱을 계산하는 함수
# 이는 수학적으로 2 ** 3과 같다
pow(2, 3)

# pow() 함수는 세 번째 인자도 받을 수 있습니다
pow(2, 3, 5)  # 결과: 8 % 5 = 3
#2 ** 3 = 8, 그리고 8 % 5 = 3
#이렇게 pow(a, b, c)는 (a ** b) % c 계산도 할 수 있다