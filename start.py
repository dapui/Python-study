# 나눗셈 연산자
5 / 2

# 정수 나눗셈 연산자
# 5를 2로 나눈 몫만 반환합니다
5 // 2

# 나머지 연산자
# a % b는 a를 b로 나눴을 때의 나머지를 반환합니다
25 % 7

